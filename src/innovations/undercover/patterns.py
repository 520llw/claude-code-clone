"""
Patterns - Detection patterns for sensitive information.
"""

import re


# Patterns that might identify Anthropic or Claude
SENSITIVE_PATTERNS = {
    # Company names
    'anthropic': re.compile(r'\banthropic\b', re.IGNORECASE),
    'claude': re.compile(r'\bclaude\b', re.IGNORECASE),
    'claude_code': re.compile(r'\bclaude[_-]?code\b', re.IGNORECASE),
    
    # Internal codenames
    'kairos': re.compile(r'\bkairos\b', re.IGNORECASE),
    'buddy_system': re.compile(r'\bbuddy[_-]?system\b', re.IGNORECASE),
    
    # Email patterns
    'anthropic_email': re.compile(r'[\w.-]+@anthropic\.com', re.IGNORECASE),
    'claude_email': re.compile(r'[\w.-]+@claude\.ai', re.IGNORECASE),
    
    # Internal URLs
    'internal_url': re.compile(r'https?://(?:internal|corp|git)\.anthropic\.\w+', re.IGNORECASE),
    
    # API keys (general pattern)
    'api_key': re.compile(r'\b(sk-[a-zA-Z0-9]{20,})\b'),
    
    # Internal identifiers
    'employee_id': re.compile(r'\bEMP-\d{5,}\b'),
    'ticket_id': re.compile(r'\bANTH-\d{4,}\b'),
    
    # TODO/FIXME with internal references
    'internal_todo': re.compile(r'\b(TODO|FIXME|HACK|XXX)\b.*?(anthropic|claude|internal)', re.IGNORECASE),
    
    # Copyright headers
    'copyright': re.compile(r'copyright.*anthropic', re.IGNORECASE),
    
    # Author tags
    'author_tag': re.compile(r'@author.*$', re.MULTILINE | re.IGNORECASE),
    
    # Machine-generated markers
    'ai_generated': re.compile(r'\b(generated by|created with|written by).*(?:claude|ai|assistant)', re.IGNORECASE),
}


